#include <iostream>
using namespace std ;
int main ()
{
	int a = 10 , b = 5 ;
	
	if ( a == b )
	{
		cout << " a = b: 1 " << endl ;
	}
	
	else if ( a != b )
	{
		cout << " a != b: 1 " << endl ;
	}
	
	else if ( a <= b )
	{
		cout << " a <= b: 1 " << endl ;
	}
	
	else 
	{
		cout << " a >= b: 1 " << endl ;
	}
	
	return 0 ;

}