#include <iostream>
using namespace std ;
double LENGTH = 5.84 ; // Global Variable
int main()
{
	const double WIDTH = 1.36 ; // Declaring a constant variable
	double perimeter, area ;
	perimeter = 2 * ( LENGTH + WIDTH ) ;
	area = LENGTH * WIDTH ;
	cout << "Perimeter of rectangle = "<< perimeter << endl ;
	cout << "Area of rectangle = " << area ;
} 













